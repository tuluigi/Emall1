            CGContextDrawImage(ctx, CGRectMake(0,0,aImage.size.width,aImage.size.height), aImage.CGImage);
            break;
    }
    
    CGImageRef cgimg = CGBitmapContextCreateImage(ctx);
    UIImage *img = [UIImage imageWithCGImage:cgimg];
    CGContextRelease(ctx);
    CGImageRelease(cgimg);
    
    return img;
}
- (CGFloat)sizeKBOfImage {
    NSData *imageData = UIImageJPEGRepresentation(self, 1);
    CGFloat length = [imageData length]/1024;
    return length;
}

- (CGFloat)sizeMBOfImage {
    CGFloat length = [self sizeKBOfImage]/1024;
    return length;
}

- (NSData *)fixImageUploadSize:(CGFloat)topSize unit:(NSString *)unit{
    NSData *fixedImageData = UIImagePNGRepresentation(self);
    
    CGFloat length = [fixedImageData length];
    if ([unit isEqualToString:@"KB"]) {
        length = [self sizeKBOfImage];
        
    } else if ([unit isEqualToString:@"MB"]) {
        length = [self sizeMBOfImage];
    }
    
    if (topSize <= length) {
        CGFloat scale = topSize/length;
       fixedImageData = UIImageJPEGRepresentation(self, scale);
    }
    return fixedImageData;
}

- (NSData *)dataByComperssionImage
{
    UIImage *image = [self scaledImage:self];
    NSData *imageData = UIImageJPEGRepresentation(image, 0.8);
    return imageData;
}

- (UIImage *)scaledImage:(UIImage *)image {
    if (image.size.width <= 1024) {
        return image;
    }
    CGSize newSize = CGSizeMake(1024, (image.size.height * 1024 ) / image.size.width);
    UIGraphicsBeginImageContext(newSize);
    [image drawInRect:CGRectMake(0, 0, newSize.width, newSize.height)];
    UIImage *newImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return newImage;
}

@end
